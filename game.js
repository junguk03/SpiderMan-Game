// ==================== RENDERING ====================
function render() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    // Background
    const grad = ctx.createLinearGradient(0, 0, 0, canvas.height);
    grad.addColorStop(0, '#0f0c29');
    grad.addColorStop(0.5, '#302b63');
    grad.addColorStop(1, '#24243e');
    ctx.fillStyle = grad;
    ctx.fillRect(0, 0, canvas.width, canvas.height);

    // Only draw game elements when playing or paused
    if (gameState !== 'playing' && gameState !== 'paused') return;

    ctx.save();
    ctx.translate(-camera.x, -camera.y);

    // Platforms
    ctx.fillStyle = '#4a4a6a';
    for (const p of platforms) {
        ctx.fillRect(p.x, p.y, p.w, p.h);
        ctx.fillStyle = '#6a6a8a';
        ctx.fillRect(p.x, p.y, p.w, 4);
        ctx.fillStyle = '#4a4a6a';
    }

    // 움직이는 발판
    for (const mp of movingPlatforms) {
        ctx.save();
        ctx.shadowColor = '#4aa8ff';
        ctx.shadowBlur = 12;
        ctx.fillStyle = '#3a8ad4';
        ctx.fillRect(mp.x, mp.y, mp.w, mp.h);
        ctx.fillStyle = '#6abfff';
        ctx.fillRect(mp.x, mp.y, mp.w, 4);
        ctx.strokeStyle = '#2a6aaa';
        ctx.lineWidth = 1;
        ctx.strokeRect(mp.x, mp.y, mp.w, mp.h);
        ctx.restore();
    }

    // Walls
    ctx.fillStyle = '#5a5a7a';
    for (const w of walls) {
        ctx.fillRect(w.x, w.y, w.w, w.h);
    }

    // Hazards (빨간 발판/벽) - 깔끔한 빨간색 직사각형
    for (const h of hazards) {
        ctx.save();
        // 글로우 효과
        ctx.shadowColor = '#ff0000';
        ctx.shadowBlur = 20;
        // 메인 빨간 색상
        ctx.fillStyle = '#e74c3c';
        ctx.fillRect(h.x, h.y, h.w, h.h);
        // 테두리
        ctx.strokeStyle = '#c0392b';
        ctx.lineWidth = 2;
        ctx.strokeRect(h.x, h.y, h.w, h.h);
        // 하이라이트 (위쪽)
        ctx.fillStyle = '#ff6b6b';
        ctx.fillRect(h.x + 2, h.y + 2, h.w - 4, 3);
        ctx.restore();
    }

    // Turrets - 동그란 기계
    for (const t of turrets) {
        ctx.save();
        ctx.shadowColor = '#ff4444';
        ctx.shadowBlur = 15;
        // 본체 (원형)
        ctx.beginPath();
        ctx.arc(t.x, t.y, t.radius, 0, Math.PI * 2);
        ctx.fillStyle = '#555';
        ctx.fill();
        ctx.strokeStyle = '#ff4444';
        ctx.lineWidth = 2;
        ctx.stroke();
        // 포구 표시 (발사 방향)
        ctx.beginPath();
        let mx = t.x, my = t.y;
        if (t.dir === 'left') mx -= t.radius;
        else if (t.dir === 'right') mx += t.radius;
        else if (t.dir === 'up') my -= t.radius;
        else if (t.dir === 'down') my += t.radius;
        else { mx += t.radius * 0.7; my += t.radius * 0.7; }
        ctx.arc(mx, my, 4, 0, Math.PI * 2);
        ctx.fillStyle = '#ff6666';
        ctx.fill();
        // 깜빡임 (발사 직전)
        if (t.timer > (t.interval || 120) - 20) {
            ctx.beginPath();
            ctx.arc(t.x, t.y, t.radius + 3, 0, Math.PI * 2);
            ctx.strokeStyle = `rgba(255, 68, 68, ${0.3 + 0.7 * Math.sin(t.timer * 0.5)})`;
            ctx.lineWidth = 2;
            ctx.stroke();
        }
        ctx.restore();
    }

    // Projectiles - 발사체
    for (const p of projectiles) {
        ctx.save();
        ctx.shadowColor = '#ff4444';
        ctx.shadowBlur = 10;
        ctx.beginPath();
        ctx.arc(p.x, p.y, p.radius, 0, Math.PI * 2);
        ctx.fillStyle = '#ff4444';
        ctx.fill();
        // 발사체 꼬리
        ctx.beginPath();
        ctx.arc(p.x - p.vx * 2, p.y - p.vy * 2, p.radius * 0.6, 0, Math.PI * 2);
        ctx.fillStyle = 'rgba(255, 68, 68, 0.4)';
        ctx.fill();
        ctx.restore();
    }

    // Anchors
    for (const a of anchors) {
        ctx.beginPath();
        ctx.arc(a.x, a.y, a.radius * 1.8, 0, Math.PI * 2);
        ctx.fillStyle = 'rgba(102, 126, 234, 0.3)';
        ctx.fill();
        ctx.beginPath();
        ctx.arc(a.x, a.y, a.radius, 0, Math.PI * 2);
        ctx.fillStyle = '#667eea';
        ctx.fill();
    }

    // Buttons - 버튼 렌더링
    for (const btn of buttons) {
        ctx.save();
        if (btn.pressed) {
            // 눌린 버튼 - 납작하게
            ctx.fillStyle = '#27ae60';
            ctx.fillRect(btn.x, btn.y + 10, btn.width, 5);
        } else {
            // 안 눌린 버튼 - 튀어나온 형태
            ctx.fillStyle = '#e74c3c';
            ctx.shadowColor = '#e74c3c';
            ctx.shadowBlur = 10;
            ctx.fillRect(btn.x, btn.y, btn.width, btn.height);
            // 버튼 위 하이라이트
            ctx.fillStyle = '#ff6b6b';
            ctx.fillRect(btn.x + 2, btn.y + 2, btn.width - 4, 4);
        }
        ctx.restore();
    }

    // Stars - 활성화/비활성화 상태에 따라 다르게 렌더링
    for (const s of stars) {
        if (s.collected) continue;
        ctx.save();
        ctx.translate(s.x, s.y);

        // 별 모양 경로 생성
        ctx.beginPath();
        for (let i = 0; i < 10; i++) {
            const angle = (i * Math.PI) / 5 - Math.PI / 2;
            const r = i % 2 === 0 ? s.radius : s.radius * 0.4;
            if (i === 0) ctx.moveTo(Math.cos(angle) * r, Math.sin(angle) * r);
            else ctx.lineTo(Math.cos(angle) * r, Math.sin(angle) * r);
        }
        ctx.closePath();

        if (s.active) {
            // 활성화된 별 - 채워진 금색
            ctx.fillStyle = '#ffd700';
            ctx.shadowColor = '#ffd700';
            ctx.shadowBlur = 15;
            ctx.fill();
        } else {
            // 비활성화된 별 - 윤곽선만 (회색 투명)
            ctx.strokeStyle = 'rgba(150, 150, 150, 0.5)';
            ctx.lineWidth = 2;
            ctx.setLineDash([5, 3]); // 점선
            ctx.stroke();
            ctx.setLineDash([]); // 점선 해제
        }
        ctx.restore();
    }

    // Door
    if (door) {
        ctx.fillStyle = door.open ? '#2ecc71' : '#7f8c8d';
        ctx.fillRect(door.x, door.y, door.width, door.height);
        if (door.open) {
            ctx.shadowColor = '#2ecc71';
            ctx.shadowBlur = 20;
            ctx.strokeStyle = '#2ecc71';
            ctx.lineWidth = 3;
            ctx.strokeRect(door.x, door.y, door.width, door.height);
            ctx.shadowBlur = 0;
        }
    }

    // Grapple
    if (grapple.active) {
        ctx.strokeStyle = '#ffd700';
        ctx.lineWidth = 3;
        ctx.beginPath();
        ctx.moveTo(player.x + player.width / 2, player.y + 5);
        ctx.lineTo(grapple.attached ? grapple.anchorX : grapple.x, grapple.attached ? grapple.anchorY : grapple.y);
        ctx.stroke();
    }

    // Player (stickman)
    drawPlayer();

    ctx.restore();

    // Tutorial overlay
    if (LEVELS[currentLevel]?.isTutorial) drawTutorial();

    // Button level tutorial - 버튼 레벨 안내
    if (LEVELS[currentLevel]?.isButtonLevel) drawButtonTutorial();

    // Hazard level tutorial - 위험요소 안내
    if (LEVELS[currentLevel]?.tutorial && hazards.length > 0) drawHazardTutorial();
}

function drawPlayer() {
    const x = player.x + player.width / 2;
    const y = player.y + player.height / 2;

    ctx.save();
    ctx.translate(x, y);
    if (!player.facingRight) ctx.scale(-1, 1);

    ctx.strokeStyle = '#ffd700';
    ctx.fillStyle = '#ffd700';
    ctx.lineWidth = 4;
    ctx.lineCap = 'round';
    ctx.shadowColor = '#ffd700';
    ctx.shadowBlur = 10;

    const head = 8, body = 18, limb = 14;
    let armL = 0.5, armR = 0.5, legL = 0.3, legR = 0.3;
    let bodyTilt = 0;
    let headOffset = 0;

    const t = Date.now() * 0.012;
    const speed = Math.abs(player.vx);

    if (grapple.attached) {
        // 줄타기: 몸이 줄 방향으로 기울어짐
        const ropeAngle = Math.atan2(
            grapple.anchorY - (player.y + 5),
            grapple.anchorX - (player.x + player.width / 2)
        );
        bodyTilt = Math.sin(ropeAngle) * 0.3;
        armL = -1.2;
        armR = -1.0;
        legL = 0.4 + Math.sin(t * 2) * 0.2;
        legR = 0.4 - Math.sin(t * 2) * 0.2;
    } else if (player.onWall !== 0) {
        // 벽타기: 벽에 붙은 자세
        armL = -0.3;
        armR = -0.5;
        legL = 0.2;
        legR = 0.5;
        bodyTilt = player.onWall * 0.15;
    } else if (!player.onGround) {
        if (player.vy < -5) {
            // 점프 상승: 팔 위로, 다리 웅크림
            armL = -0.6;
            armR = -0.4;
            legL = 0.8;
            legR = 0.9;
        } else if (player.vy > 3) {
            // 낙하: 팔 벌리고, 다리 펴짐
            armL = 1.0 + Math.sin(t * 3) * 0.2;
            armR = 1.2 + Math.sin(t * 3 + 1) * 0.2;
            legL = 0.2;
            legR = 0.4;
        } else {
            // 공중 유지
            armL = 0.8;
            armR = 0.9;
            legL = 0.5;
            legR = 0.6;
        }
    } else if (speed > 0.5) {
        // 걷기/뛰기 애니메이션
        const walkSpeed = speed > 3 ? 0.025 : 0.018;
        const walkT = Date.now() * walkSpeed;
        const intensity = Math.min(speed / 4, 1);

        // 팔다리 교차 움직임
        armL = Math.sin(walkT) * 0.7 * intensity;
        armR = Math.sin(walkT + Math.PI) * 0.7 * intensity;
        legL = Math.sin(walkT + Math.PI) * 0.5 * intensity;
        legR = Math.sin(walkT) * 0.5 * intensity;

        // 달릴 때 몸 약간 앞으로
        bodyTilt = intensity * 0.1;
        headOffset = Math.sin(walkT * 2) * 1.5 * intensity;
    } else {
        // 서있기: 가만히 숨쉬는 모션
        const breathe = Math.sin(t * 0.5) * 0.05;
        armL = 0.4 + breathe;
        armR = 0.5 + breathe;
        legL = 0.2;
        legR = 0.25;
    }

    // 몸 기울기 적용
    ctx.rotate(bodyTilt);

    // 머리
    ctx.beginPath();
    ctx.arc(0, -body - head + headOffset, head, 0, Math.PI * 2);
    ctx.fill();

    // 몸통
    ctx.beginPath();
    ctx.moveTo(0, -body);
    ctx.lineTo(0, 0);
    ctx.stroke();

    // 팔
    ctx.beginPath();
    ctx.moveTo(0, -body + 3);
    ctx.lineTo(limb * Math.cos(armL), -body + 3 + limb * Math.sin(armL));
    ctx.moveTo(0, -body + 3);
    ctx.lineTo(-limb * Math.cos(armR), -body + 3 + limb * Math.sin(armR));
    ctx.stroke();

    // 다리
    ctx.beginPath();
    ctx.moveTo(0, 0);
    ctx.lineTo(limb * 0.9 * Math.cos(0.3 + legL), limb * 0.9 * Math.sin(0.3 + legL) + 3);
    ctx.moveTo(0, 0);
    ctx.lineTo(-limb * 0.9 * Math.cos(0.3 + legR), limb * 0.9 * Math.sin(0.3 + legR) + 3);
    ctx.stroke();

    ctx.restore();
}

