// ==================== MOBILE CONTROLS ====================
function initMobileControls() {
    const controls = document.getElementById('mobile-controls');
    if (!controls) return;
    controls.style.display = 'block';

    // 줄 조준용: 가장 가까운 앵커를 향해 자동 조준
    function aimNearestAnchor() {
        const px = player.x + player.width / 2;
        const py = player.y + player.height / 2;
        let nearest = null, minDist = Infinity;
        for (const a of anchors) {
            const dist = Math.hypot(a.x - px, a.y - py);
            if (dist < CONFIG.maxRopeLength && dist < minDist) {
                minDist = dist;
                nearest = a;
            }
        }
        if (nearest) {
            mouseX = nearest.x;
            mouseY = nearest.y;
        }
    }

    // 통합 액션 버튼 (땅=점프, 공중=줄)
    const actionBtn = document.getElementById('action-btn');
    const actionLabel = document.getElementById('action-label');
    const actionIconJump = document.getElementById('action-icon-jump');
    const actionIconGrapple = document.getElementById('action-icon-grapple');
    let lastActionMode = 'jump';

    // 버튼 상태 업데이트 (매 프레임)
    function updateActionButton() {
        const mode = player.onGround ? 'jump' : 'grapple';
        if (mode !== lastActionMode) {
            lastActionMode = mode;
            if (mode === 'jump') {
                actionLabel.textContent = '점프';
                if (actionIconJump) actionIconJump.style.display = 'none';
                if (actionIconGrapple) actionIconGrapple.style.display = 'none';
                actionLabel.style.display = '';
            } else {
                actionLabel.style.display = 'none';
                if (actionIconJump) actionIconJump.style.display = 'none';
                if (actionIconGrapple) actionIconGrapple.style.display = '';
            }
        }
        requestAnimationFrame(updateActionButton);
    }
    updateActionButton();

    if (actionBtn) {
        actionBtn.addEventListener('touchstart', (e) => {
            e.preventDefault();
            e.stopPropagation();
            actionBtn.classList.add('active');
            if (player.onGround) {
                keys.jump = true;
            } else {
                keys.grapple = true;
                aimNearestAnchor();
                if (gameState === 'playing') shootGrapple();
            }
        }, { passive: false });
        actionBtn.addEventListener('touchend', (e) => {
            e.preventDefault();
            e.stopPropagation();
            actionBtn.classList.remove('active');
            keys.jump = false;
            keys.grapple = false;
            releaseGrapple();
        }, { passive: false });
        actionBtn.addEventListener('touchcancel', () => {
            actionBtn.classList.remove('active');
            keys.jump = false;
            keys.grapple = false;
            releaseGrapple();
        });
    }

    // 화면 좌우 절반 터치로 이동
    let moveTouchId = null;
    canvas.addEventListener('touchstart', (e) => {
        if (gameState !== 'playing') return;
        for (let i = 0; i < e.changedTouches.length; i++) {
            const touch = e.changedTouches[i];
            // 오른쪽 하단 액션 버튼 영역 무시 (우측 25% + 하단 30%)
            const rx = touch.clientX / window.innerWidth;
            const ry = touch.clientY / window.innerHeight;
            if (rx > 0.75 && ry > 0.5) continue;
            // 왼쪽 상단 메뉴 버튼 영역 무시
            if (rx < 0.15 && ry < 0.15) continue;

            e.preventDefault();
            moveTouchId = touch.identifier;
            const half = window.innerWidth / 2;
            if (touch.clientX < half) {
                keys.left = true; keys.right = false;
            } else {
                keys.right = true; keys.left = false;
            }
        }
    }, { passive: false });

    canvas.addEventListener('touchmove', (e) => {
        if (gameState !== 'playing') return;
        for (let i = 0; i < e.changedTouches.length; i++) {
            const touch = e.changedTouches[i];
            if (touch.identifier === moveTouchId) {
                e.preventDefault();
                const half = window.innerWidth / 2;
                if (touch.clientX < half) {
                    keys.left = true; keys.right = false;
                } else {
                    keys.right = true; keys.left = false;
                }
            }
        }
    }, { passive: false });

    canvas.addEventListener('touchend', (e) => {
        for (let i = 0; i < e.changedTouches.length; i++) {
            if (e.changedTouches[i].identifier === moveTouchId) {
                moveTouchId = null;
                keys.left = false; keys.right = false;
            }
        }
    }, { passive: false });

    canvas.addEventListener('touchcancel', (e) => {
        for (let i = 0; i < e.changedTouches.length; i++) {
            if (e.changedTouches[i].identifier === moveTouchId) {
                moveTouchId = null;
                keys.left = false; keys.right = false;
            }
        }
    }, { passive: false });

    // 메뉴 버튼 (왼쪽 위 ☰)
    const menuBtn = document.getElementById('menu-btn');
    if (menuBtn) {
        menuBtn.addEventListener('touchstart', (e) => {
            e.preventDefault();
            e.stopPropagation();
            if (gameState === 'playing') pauseGame();
            else if (gameState === 'paused') resumeGame();
        }, { passive: false });
    }

    // 풀스크린 (상태바/내비게이션바 숨기기)
    function requestFullscreen() {
        const el = document.documentElement;
        if (el.requestFullscreen) el.requestFullscreen();
        else if (el.webkitRequestFullscreen) el.webkitRequestFullscreen();
    }
    // 첫 터치 시 풀스크린 시도
    document.addEventListener('touchstart', function onFirstTouch() {
        requestFullscreen();
        document.removeEventListener('touchstart', onFirstTouch);
    }, { once: true });
}

// ==================== GAME LOOP ====================
function gameLoop() {
    if (gameState === 'playing') updatePhysics();
    render();
    requestAnimationFrame(gameLoop);
}

